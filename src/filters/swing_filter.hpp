# pragma	once
# include	"smooth_filter.hpp"

//****************************************************************************//
//      Class "SwingFilter"                                                   //
//****************************************************************************//
class SwingFilter : public SmoothFilter
{
//============================================================================//
//      Public methods                                                        //
//============================================================================//
public:

//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~//
//      Constructor                                                           //
//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~//
	SwingFilter (
		size_t points				// Count of neighbor points to smooth by
	) :	SmoothFilter (points)
	{
		Array::Sub (impulse.data(), impulse.size(), 1.0 / impulse.size());
	}
};

//****************************************************************************//
//      Translate the object to a string                                      //
//****************************************************************************//
ostream& operator << (ostream &stream, const SwingFilter &object)
{
	stream << object.Summary ("Swing filter");
	return stream;
}
